// 6. Some os números de 1 a 100, mas pare se a soma passar de 400.

let contador =1 // inicialização da variavel 
let soma = 0

while(contador <= 100 && soma <=400 ){// setor a condição de repetição
    soma = soma +contador
        
    contador++  // modicação da contagem 
      
}
alert("contador: " +contador+ "soma: "+ soma)

    

